static void Usage(char* execName){
#ifdef __ENC
	printf("%s - Encodes .ez files for EZ2DJ 7th Trax v2.0\n", execName);
#else
	printf("%s - Decodes .ez files from EZ2DJ 7th Trax v2.0\n", execName);
#endif
	printf("Usage: %s [file.ez]\n", execName);
}

int main(int argc, char** argv){
	FILE *ezFile;
	long filesize;
	char *fileBuf;

	if(argc <= 1){
		Usage(argv[0]);
		exit(EXIT_FAILURE);
	}

	ezFile = fopen(argv[1],"r+b");
	if(ezFile == NULL){
		perror("Error opening file");
		exit(EXIT_FAILURE);
	}

	/* get filesize */
	fseek(ezFile, 0, SEEK_END);
	filesize = ftell(ezFile);
	fseek(ezFile, 0, SEEK_SET);

#ifdef __ENC
	/* before encoding, we need to reverse the file */
	fileBuf = (char*)calloc((uint32_t)filesize,1);
	if(fileBuf == NULL){
		printf("%s: Unable to allocate memory for file encoding\n",argv[0]);
		fclose(ezFile);
		exit(EXIT_FAILURE);
	}
	for(int i = 0; i < filesize; i++){
		fseek(ezFile, i*-1, SEEK_END);
		fileBuf[i] = fgetc(ezFile);
	}
	fseek(ezFile, 0, SEEK_SET);
#endif

	/* do that encoding thing */
	int keyIndex = 0;
	for(int i = 0; i < filesize; i++){
#ifdef __ENC
		uint8_t inByte = fileBuf[i];
		inByte = (inByte + DecodeEZ_7th_2dot0[keyIndex++]) & 0xFF;
#else
		uint8_t inByte = fgetc(ezFile);
		fseek(ezFile, -1, SEEK_CUR);
		inByte = inByte - DecodeEZ_7th_2dot0[keyIndex++];
#endif
		fputc(inByte,ezFile);
		if(keyIndex >= 0x200){
			keyIndex = 0;
		}
	}

#ifndef __ENC
	/* after decoding, we need to reverse the file */
	fileBuf = (char*)calloc((uint32_t)filesize,1);
	if(fileBuf == NULL){
		printf("%s: Unable to allocate memory for file decoding\n",argv[0]);
		fclose(ezFile);
		exit(EXIT_FAILURE);
	}
	for(int i = 0; i < filesize; i++){
		fseek(ezFile, i*-1, SEEK_END);
		fileBuf[i] = fgetc(ezFile);
	}
	fseek(ezFile, 0, SEEK_SET);
	for(int i = 1; i <= filesize; i++){
		fputc(fileBuf[i],ezFile);
	}
#endif

	fclose(ezFile);
#ifdef __ENC
	printf("%s: Successfully encoded %s\n", argv[0],argv[1]);;
#else
	printf("%s: Successfully decoded %s\n", argv[0],argv[1]);;
#endif

	if(fileBuf != NULL){
		free(fileBuf);
	}

	return EXIT_SUCCESS;
}
